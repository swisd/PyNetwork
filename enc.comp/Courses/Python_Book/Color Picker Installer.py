                self.hsv_entry.delete (0, END)
                self.hsv_entry.insert (0, color_hsv)
                self.wavelength_entry.delete (0, END)
                self.wavelength_entry.insert (0, w)
                self.frequency_entry.delete (0, END)
                self.frequency_entry.insert (0, f)
                self.full_entry.delete(0, END)
                self.full_entry.insert(0, freqw)


